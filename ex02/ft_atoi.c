int		ft_atoi(char *str)
{
	int		k;
	char	*p;

	k = 0;
	p = str;
	while ((*p >= '0') && (*p <= '9'))
	{
		k = k * 10 + (*str - '0');
		str++;
	}
	return (k);
}
