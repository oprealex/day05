char		*ft_strlowcase(char *str)
{
	int		i;

	i = 0;
	while (*str)
	{
		if ((str[i] >= 'A') && (str[i] <= 'Z'))
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}
